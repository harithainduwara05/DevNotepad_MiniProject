/**
 * DevNotepad Material3 Theme.
 *
 * Supports dynamic color on Android 12+ (falls back to custom palette).
 * Automatically follows system dark/light mode unless overridden.
 *
 * @param darkTheme Whether to use the dark color scheme.
 * @param dynamicColor Whether to use Material You dynamic colors (Android 12+).
 */
@Composable
fun DevNotepadTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false, // Default OFF — we want our curated IDE palette
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        // Use Material You dynamic colors if available and enabled
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context)
            else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = DevNotepadTypography,
        content = content
    )
}
